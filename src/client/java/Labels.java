/**
 * Provide a String for the labels
 */
public class Labels
{

    /**
     * Label if Client try to connect to the server
     * @return connect message
     */
    public static String labelConnecting()
    {
        return "Connecting...";
    }

    /**
     * Label find game
     * @return find game message
     */
    public static String labelFindGame()
    {
        return "Find new game...";
    }

    /**
     * Label Your move
     * @return Your move message
     */
    public static String labelYourMove()
    {
        return "Your move";
    }

    /**
     * Label if no game was found
     * @return No game found message
     */
    public static String labelNoGameFound()
    {
        return "No game found";
    }

    /**
     * Label if a client try to rejoin a game
     * @return rejoin message
     */
    public static String labelRejoin()
    {
        return "Rejoining";
    }
}
